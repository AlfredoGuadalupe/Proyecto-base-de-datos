CREATE TABLE MANTENIMIENTO(
nombreSuc VARCHAR2(18),
numCons NUMBER(4),
TipoEq VARCHAR2(18),
FechaMant DATE
);

CREATE OR REPLACE TRIGGER tgMantenimiento
AFTER INSERT OR UPDATE OF FechaMant
ON EQUIPAMIENTO
FOR EACH ROW
BEGIN
	INSERT INTO MANTENIMIENTO (nombreSuc,numCons,TipoEq,FechaMant)
	VALUES (:NEW.nombreSuc,:NEW.numCons,:NEW.TipoEq,:NEW.FechaMant);
END tgMantenimiento;
