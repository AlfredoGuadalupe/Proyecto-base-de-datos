CREATE OR REPLACE PROCEDURE spConsulta(vPaciente IN NUMBER
,vTrat IN VARCHAR,vConsultorio IN NUMBER,vFin IN CHAR)
AS
    vSuc VARCHAR(18);
    vOdo NUMBER(20);
    vConsulta NUMBER(30);
BEGIN
    SELECT MAX(NUMCONSULTA) INTO vConsulta
    FROM HISTCLINICA
    WHERE CEDULAIDE=vPaciente;
    SELECT NOMBRESUC,CEDULAODO INTO vSuc,vOdo
    FROM AFILIADO
    WHERE CEDULAIDE=vPaciente;
    IF (vConsulta IS NULL) THEN
        vConsulta := 1;
    ELSE
        vConsulta := vConsulta+1;
    END IF;
    INSERT INTO HISTCLINICA (CEDULAIDE,NUMCONSULTA,NOMBRESUC,
    CEDULAODO,NUMCONS,ABVNOM,FINALIZACION)
    VALUES(vPaciente,vConsulta,vSuc,vOdo,vConsultorio,vTrat,vFin);
end spConsulta;
