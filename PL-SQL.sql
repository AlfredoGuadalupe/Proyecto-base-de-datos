CREATE OR REPLACE VIEW vwSucTrat
AS
SELECT NOMBRESUC AS SUCURSAL,CIUDADSUC||', '||CALLESUC||' #'||NUMEROSUC AS DIRECCION
,ABVNOM AS TRATAMIENTO
FROM SUCURSAL JOIN SUCODO USING(NOMBRESUC)
JOIN ODONTOLOGO USING(CEDULAODO)
JOIN REALIZA USING(CEDULAODO);

SELECT * FROM vwSucTrat;

CREATE OR REPLACE FUNCTION ftDescuento(vAf IN NUMBER,vMes IN DATE)
RETURN NUMBER
IS
    vPago NUMBER(6);
    vReb NUMBER(4,2);
BEGIN
    SELECT TOTAL_PAGOS,PORCREB INTO vPago,vReb FROM
    GRUPAL JOIN vwMesAfCos USING(CEDULAIDE)
    WHERE MES = TO_CHAR(vMes,'MONTH/YY') AND CEDULAIDE=vAf;
    vPago:=vPago*(1-vReb/100);
    RETURN(vPago);
END ftDescuento;
/

