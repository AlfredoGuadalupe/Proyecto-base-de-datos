CREATE OR REPLACE VIEW vwMesAfCos
AS
SELECT CEDULAIDE,NOMAF||' '||APPATAF||' '||APMATAF AS AFILIADO,
TO_CHAR(FECHACONS,'MONTH/YY') AS MES,SUM(COSTO) AS TOTAL_PAGOS FROM
HISTCLINICA JOIN TRATAMIENTO USING(ABVNOM) JOIN AFILIADO USING(CEDULAIDE)
GROUP BY CEDULAIDE,NOMAF||' '||APPATAF||' '||APMATAF,TO_CHAR(FECHACONS,'MONTH/YY');

SELECT * FROM vwMesAfCos;

