CREATE OR REPLACE VIEW vwDatosOdontologo
AS
SELECT CEDULAODO AS CEDULA,NOMOD||' '||APPATOD||' '||APMATOD AS ODONTOLOGO,
ESPECIALIDAD,ABVNOM AS TRATAMIENTO,NOMBRESUC AS SUCURSAL
FROM SUCODO JOIN ODONTOLOGO USING(CEDULAODO)
JOIN REALIZA USING(CEDULAODO)
ORDER BY CEDULAODO;

SELECT * FROM vwDatosOdontologo;

CREATE OR REPLACE VIEW vwMontoSucTrat
AS
SELECT NOMBRESUC AS SUCURSAL,TO_CHAR(FECHACONS,'MONTH/YY') AS MES,SUM(COSTO) AS TOTAL_PAGOS FROM
HISTCLINICA JOIN TRATAMIENTO USING(ABVNOM)
GROUP BY NOMBRESUC,TO_CHAR(FECHACONS,'MONTH/YY')
ORDER BY NOMBRESUC;

